        $('.other_nav').on('click', function() {
            let elem_id = this.getAttribute('href');
            elem_id = elem_id.substring(1);
            const other_panes = document.getElementsByClassName('other_pane');

            for(const pane of other_panes) {
                pane.classList.remove("active","show");

                if(pane.getAttribute('id') != elem_id)
                pane.classList.add("d-none");
            }

            document.getElementById(elem_id).classList.remove("d-none");
            document.getElementById(elem_id).classList.add("active","show");
            //$(elem_id).removeClass("d-none").addClass("active show");
        })
